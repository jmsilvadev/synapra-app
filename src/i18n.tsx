type I18nContextValue = {
  language: AppLanguage;
  setLanguage: (language: AppLanguage) => void;
  t: (key: string, vars?: Record<string, string | number>) => string;
  locale: string;
};

const I18nContext = createContext<I18nContextValue | undefined>(undefined);

function interpolate(template: string, vars?: Record<string, string | number>) {
  if (!vars) {
    return template;
  }

  return template.replace(/\{\{(\w+)\}\}/g, (_, key) => String(vars[key] ?? ""));
}

export function getStoredLanguage(): AppLanguage {
  const stored = typeof window !== "undefined" ? window.localStorage.getItem(STORAGE_KEY) : null;
  if (stored === "pt" || stored === "es" || stored === "en" || stored === "fr") {
    return stored;
  }
  return DEFAULT_LANGUAGE;
}

export function translate(language: AppLanguage, key: string, vars?: Record<string, string | number>) {
  const catalog = messages[language] || messages[DEFAULT_LANGUAGE];
  const template = catalog[key] || messages[DEFAULT_LANGUAGE][key] || key;
  return interpolate(template, vars);
}

export function getLocale(language: AppLanguage) {
  return localeByLanguage[language];
}

export function getLanguageOptions() {
  return (Object.keys(languageLabels) as AppLanguage[]).map((language) => ({
    value: language,
    label: languageLabels[language],
  }));
}

export const I18nProvider: React.FC<{ children: React.ReactNode }> = ({ children }) => {
  const [language, setLanguageState] = useState<AppLanguage>(getStoredLanguage);

  const setLanguage = (nextLanguage: AppLanguage) => {
    setLanguageState(nextLanguage);
    localStorage.setItem(STORAGE_KEY, nextLanguage);
  };

  useEffect(() => {
    document.documentElement.lang = getLocale(language);
  }, [language]);

  const value = useMemo<I18nContextValue>(
    () => ({
      language,
      setLanguage,
      t: (key, vars) => translate(language, key, vars),
      locale: getLocale(language),
    }),
    [language]
  );

  return <I18nContext.Provider value={value}>{children}</I18nContext.Provider>;
};

export function useI18n() {
  const context = useContext(I18nContext);
  if (!context) {
    throw new Error("useI18n must be used within I18nProvider");
  }
  return context;
}
